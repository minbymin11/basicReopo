
public class StringCompareMain {

	public static void main(String[] args) {
		
		String str1="JAVA";
		String str2="JAVA";
		
		String str3=new String("JAVA");
		String str4=new String("JAVA");
		
		if(str1.equals(str2)){
			System.out.println("str1과 str2의 문자열의 값이 같다");
		}
		if(str1.equals(str3)){
			System.out.println("str1과 str3의 문자열의 값이 같다");
			
		}
		if(str1.equals(str4)){
			System.out.println("str1과 str4의 문자열의 값이 같다");
			
		}
		
		if(str1.equals("JAVA")){
			System.out.println("str1과 \"JAVA\" 의 문자열의 값이 같다");
			
		}
		if("JAVA".equals("JAVA")){
			System.out.println("\"JAVA\"과 \"JAVA\" 의 문자열의 값이 같다");
			
		}
		if(!str4.equals("java")){
			System.out.println("str4과 \"java\" 의 문자열의 값이 같지않다");
			
		}
		//대소문자 무시하고 비교
		if(str4.equalsIgnoreCase("java")){
			System.out.println("str4과 \"java\" 의 문자열의 값이 같다(대소문자 무시)");
		}
		
	}

}
